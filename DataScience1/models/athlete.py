class Athlete:
    def __init__(
        self,
        name: str,
        gender: str,
        age: int,
        height: float,
        weight: float,
        team: str,
        noc: str,
        year: int,
        season: str,
        city: str,
        sport: str,
        event: str,
        medal: str,
    ):
        self.name = name
        self.gender = gender
        self.age = age
        self.height = height
        self.weight = weight
        self.team = team
        self.noc = noc
        self.year = year
        self.season = season
        self.city = city
        self.sport = sport
        self.event = event
        self.medal = medal
